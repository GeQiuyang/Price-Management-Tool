let counter = 0;

db.serialize(() => {
    db.run("BEGIN TRANSACTION");
    const stmt = db.prepare(`
        INSERT INTO products (name, category, sku, price, dealer_price, description, status) 
        VALUES (?, ?, ?, ?, ?, ?, 'active')
    `);

    for (const item of data) {
        const name = `料斗 ${item.方数} ${item.厚度}`;
        const category = '导管类';

        const fStr = item.方数.replace('方', 'F');
        const tStr = item.厚度.replace('mm', '');
        const sku = `LD-${fStr}-${tStr}-${counter++}`;

        const description = `方数: ${item.方数}, 厚度: ${item.厚度}, 重量: ${item.重量 || '未知'}, 内径: ${item.内径}, 圆柱高度: ${item.圆柱高度}, 总高: ${item.总高}`;
        const price = item.终端价;
        const dealer_price = item.经销商价;

        stmt.run(name, category, sku, price, dealer_price, description);
    }

    stmt.finalize();
    db.run("COMMIT", () => {
        console.log(`成功导入 ${data.length} 条料斗数据到导管类！`);
        db.close();
    });
});
